package edu.fa;

public class Arithmetic {
	public static void main(String[] args) {
		int a = 15;
		int b = 6;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
//		a được sử dụng trước rồi ++ sau
		System.out.println(a++);
//		a = 16
//		a được sử dụng trước rồi -- sau
		System.out.println(a--);
//		a = 15
//		a + trước rồi sử dụng sau
		System.out.println(++a);
		
	}
}
